export const getTemplateById = (id: string): FixtureTemplate | undefined => {
  return fixtureTemplates.find((t) => t.id === id)
}

export const getTemplatesByCategory = (category: FixtureCategory | 'all'): FixtureTemplate[] => {
  if (category === 'all') return fixtureTemplates
  return fixtureTemplates.filter((t) => t.category === category)
}
